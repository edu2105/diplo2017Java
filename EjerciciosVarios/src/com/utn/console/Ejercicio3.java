package com.utn.console;

public class Ejercicio3 {
	int N;
	
	Ejercicio3 (int n) {
		N = n;
	}

	void resolverEj3 () {
		System.out.println("N = " + N);
		System.out.println("N incrementado en 77 es: " + (N +77));
		System.out.println("N decrementada en 3 es: " + (N-3));
		System.out.println("N duplicada es: " + (N*2));
	}
}
